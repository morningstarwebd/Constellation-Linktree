export default function IconBubble({ link, onOpenModal }: Props) {
    const handleClick = () => {
        if (link.openModal && onOpenModal) {
            onOpenModal();
        } else {
            window.open(link.url, '_blank', 'noopener,noreferrer');
        }
    };

    return (
        <button
            className="icon-bubble"
            data-tooltip={link.label}
            aria-label={link.label}
            onClick={handleClick}
            style={{
                width: 'clamp(40px, 5.5vw, 62px)',
                height: 'clamp(40px, 5.5vw, 62px)',
                borderRadius: '50%',
                background: 'rgba(10, 20, 60, 0.55)',
                border: '1.5px solid rgba(100, 160, 255, 0.5)',
                backdropFilter: 'blur(4px)',
                boxShadow: '0 0 16px rgba(80, 140, 255, 0.25), inset 0 0 10px rgba(80, 140, 255, 0.1)',
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                padding: '0',
            }}
            onMouseEnter={(e) => {
                const el = e.currentTarget;
                el.style.background = 'rgba(30, 60, 160, 0.75)';
                el.style.borderColor = 'rgba(150, 200, 255, 0.9)';
                el.style.boxShadow = '0 0 30px rgba(100, 180, 255, 0.6), inset 0 0 15px rgba(100, 180, 255, 0.2)';
                el.style.transform = 'scale(1.15)';
            }}
            onMouseLeave={(e) => {
                const el = e.currentTarget;
                el.style.background = 'rgba(10, 20, 60, 0.55)';
                el.style.borderColor = 'rgba(100, 160, 255, 0.5)';
                el.style.boxShadow = '0 0 16px rgba(80, 140, 255, 0.25), inset 0 0 10px rgba(80, 140, 255, 0.1)';
                el.style.transform = 'scale(1)';
            }}
        >
            <span style={{ width: '45%', height: '45%', display: 'flex' }}>
                {getIcon(link.icon)}
            </span>
        </button>
    );
}
